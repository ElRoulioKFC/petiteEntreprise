<h3>Nos Objectifs:</h3>
<p id='prem'>Notre site a pour but de repérer et de sauver les animaux abandonner ou perdu.
	Pour réaliser ce projet nous avons besoin de toute la cummunauté souhaitant aider les annimaux.
</p>
<h3>Fonctionnement</h3>
<p>Pour aider les animaux nous travaillons avec des associations animalières qui sont capable de s'occuper de tout ces animaux mais aussi des citoyen, en effet notre site permet aux particuliers de signaler un animal abandonner selon ces possiblilité.
	Si cette dernière n'est pas dans la possiblité de garder l'animal, alors un signalement sera référencer sur le site afin de prévenir les association ou les membres a proximité si ils ont la possibilité de venir le chercher.
</p>
<h3>Utilité</h3>
<p>Ce site permet donc de donner une meilleur vie a ces animaux dans un nouveaux foyer mais il permet aussi de retrouver un animal perdu, en effet si vous avez perdu votre animal car il c'est enfuit, vous pouvez savoir rapidement si il a été apreçu dans votre cartier.</p> 
